package com.azizinetwork.hajde.model.backend;


public class WhatsKarma {

    private String objectId;
    private String title;
    private String content;
    private String titleAlb;
    private String contentAlb;

    public String getObjectId() {
        return objectId;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }

    public String getTitleAlb() {
        return titleAlb;
    }

    public String getContentAlb() {
        return contentAlb;
    }

    public void setObjectId(String objectId) {
        this.objectId = objectId;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public void setTitleAlb(String titleAlb) {
        this.titleAlb = titleAlb;
    }

    public void setContentAlb(String contentAlb) {
        this.contentAlb = contentAlb;
    }
}
